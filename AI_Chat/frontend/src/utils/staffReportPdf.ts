import jsPDF from 'jspdf';

export type StaffReportType = 'radiology' | 'lab' | 'general';

export interface StaffReportPdfOptions {
  content: string;
  reportType: StaffReportType;
  messageId: string;
  timestamp: string | Date;
  sourceFileName?: string;
  /** Source imaging study shown in the PDF (radiology reports). */
  referenceImage?: {
    url?: string;
    dataUrl?: string;
    label?: string;
  };
}

